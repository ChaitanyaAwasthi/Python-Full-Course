"""Type Casting"""

#from this we can change the data type str to int by using int()

#if the intput is a flat then py will round off the decimal number 
a = int('234')
print(type(a))
print(a)


#this will convert the int which is 10 in this case to 10.0 which is floating 
b = float(10)
print(b)

# we can transform numbers to strings 
c = str(23)
print(type(c))
print(c)


"""in type casting we can basically transform data types into each other"""